package chapter13;


public class Bag<T> {
  
  private T content;
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public Bag(T content) {
    this.content = content;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public T getContent() {
    return content;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public void setContent(T content) {
    this.content = content;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
  @Override
  public String toString() {
    return "Bag contains " + content + ".";
  }
  
  
  //-------------------------------------------------------------------------------------------------------------------
  
}
