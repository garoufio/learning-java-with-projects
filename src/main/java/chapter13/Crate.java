package chapter13;

public class Crate<T> {

  private T item;
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public Crate(T content) {
    this.item = content;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public T getItem() {
    return item;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public void setItem(T item) {
    this.item = item;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
}
