package chapter8;

public enum TicketType {
  INFANT(0),
  CHILD(5),
  ADULT(10),
  ELDER(7),
  DISABLED_PERSON(5),
  SCHOOL(5),
  FORBIDDEN(0),
  VIP(18);
  
  //-------------------------------------------------------------------------------------------------------------------
  
  private double price;
  
  //-------------------------------------------------------------------------------------------------------------------
  
  TicketType(double price) {
    this.price = price;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
  public double getPrice() {
    return price;
  }
  
  //-------------------------------------------------------------------------------------------------------------------
  
}
